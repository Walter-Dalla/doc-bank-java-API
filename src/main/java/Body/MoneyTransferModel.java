package Body;

public class MoneyTransferModel {
	private Integer payToId;
	private Integer myId;
	private Integer money;
	
	public Integer getMoney() {
		return money;
	}
	public void setMoney(Integer money) {
		this.money = money;
	}
	public Integer getMyId() {
		return myId;
	}
	public void setMyId(Integer myId) {
		this.myId = myId;
	}
	public Integer getPayToId() {
		return payToId;
	}
	public void setPayToId(Integer payToId) {
		this.payToId = payToId;
	}
	
	
	
}
